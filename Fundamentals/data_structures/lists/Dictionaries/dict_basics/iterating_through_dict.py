squares = {1: 1, 2: 3, 3: 9}
# iterating through the keys in the dict
for key in squares.keys():
    print(key, end=' ')

print()
# iterating through the values in the dict
for value in squares.values():
    print(value, end=' ')

print()
# iterating through the pairs in the dict
for item in squares.items():
    print(item, end='')
